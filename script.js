let opcao = parseInt(prompt('Digite uma opção: '))

switch(opcao){
    case 1: 
        document.write('Primeira Opção');

    break;
    case 2: 
        document.write('Segunda Opção');
    break;
    case 3: 
        document.write('Terceira Opção');
    break;
    default : 
        document.write('Quarta Opção');
     break;
}